(ns introduction-to-clojure.core
  (:require [bakery.core :refer :all]))

(defn add-egg []
  (grab :egg)
  (squeeze)
  (add-to-bowl))

(defn add-flour []
  (grab :cup)
  (scoop :flour)
  (add-to-bowl)
  (release))

(defn add-milk []
  (grab :cup)
  (scoop :milk)
  (add-to-bowl)
  (release))

(defn add-sugar []
  (grab :cup)
  (scoop :sugar)
  (add-to-bowl)
  (release))

(defn add-butter []
  (grab :butter)
  (add-to-bowl))

(defn scooped? [ingredient]
  (cond
    (= ingredient :milk)
    true
    (= ingredient :flour)
    true
    (= ingredient :sugar)
    true
    :else
    false))

(defn squeezed? [ingredient]
  (= ingredient :egg))

(defn simple? [ingredient]
  (= ingredient :butter))

(defn add-eggs [n]
  (dotimes [e n]
    (add-egg))
  :ok)

(defn add-flour-cups [n]
  (dotimes [e n]
    (add-flour))
  :ok)

(defn add-milk-cups [n]
  (dotimes [e n]
    (add-milk))
  :ok)

(defn add-sugar-cups [n]
  (dotimes [e n]
    (add-sugar))
  :ok)

(defn add-butters [n]
  (dotimes [e n]
    (add-butter))
  :ok)

(defn add-squeezed
  ([ingredient amount]
    (if (squeezed? ingredient)
      (do
        (dotimes [i amount]
          (grab ingredient)
          (squeeze)
          (add-to-bowl))
        :ok)
      (do
        (println "This function only works on squeezed ingredients. You asked me to squeeze" ingredient)
        :error)))
  ([ingredient]
   (add-squeezed ingredient 1)))

(defn add-scooped
  ([ingredient amount]
    (if (scooped? ingredient)
      (do
        (dotimes [i amount]
          (grab :cup)
          (scoop ingredient)
          (add-to-bowl)
          (release))
        :ok)
      (do
        (println "This function only works on scooped ingredients. You asked me to scoop" ingredient)
        :error)))
  ([ingredient]
   (add-scooped ingredient 1)))

(defn add-simple
  ([ingredient amount]
    (if (simple? ingredient)
      (do
        (dotimes [i amount]
          (grab ingredient)
          (add-to-bowl))
        :ok)
      (do
        (println "This function only works on simple ingredients. You asked me to add" ingredient)
        :error)))
  ([ingredient]
    (add-simple ingredient 1)))

(defn add
  ([ingredient]
   (add ingredient 1))
  ([ingredient amount]
   (cond
     (squeezed? ingredient)
     (add-squeezed ingredient amount)
     (scooped? ingredient)
     (add-scooped ingredient amount)
     (simple? ingredient)
     (add-simple ingredient amount)
     :else
     (do
       (println "I do not know the ingredient" ingredient)
       :error))))

(defn bake-cake []
  (add :egg 2)
  (add :flour 2)
  (add :milk)
  (add :sugar)
  (mix)
  (pour-into-pan)
  (bake-pan 25)
  (cool-pan))
